/**
 * Deployment guide generator
 *
 * Generates docs/deployment.md adapted to the selected backend, database,
 * and project structure options.
 */

import type { ProjectConfig } from '../../types/schemas'

/** Human-readable backend name for docs */
function backendLabel(backend: ProjectConfig['backend']): string {
  switch (backend) {
    case 'express-bun':
      return 'Express (Bun)'
    case 'hono-bun':
      return 'Hono (Bun)'
    case 'fastify-node':
      return 'Fastify (Node.js)'
    case 'go-fiber':
      return 'Go Fiber'
    case 'rust-axum':
    case 'rust-actix':
      return 'Rust Axum'
    case 'python-fastapi':
      return 'Python FastAPI'
    case 'none':
      return 'None (frontend only)'
  }
}

/** Host recommendations per backend */
function hostingHint(backend: ProjectConfig['backend']): string {
  switch (backend) {
    case 'express-bun':
    case 'hono-bun':
      return 'Railway, Fly.io, Render, or another Bun-friendly host'
    case 'fastify-node':
      return 'Railway, Fly.io, Render, or any Node.js host'
    case 'go-fiber':
      return 'Railway, Fly.io, Google Cloud Run, or any container host'
    case 'rust-axum':
    case 'rust-actix':
      return 'Railway, Fly.io, or any container host'
    case 'python-fastapi':
      return 'Railway, Fly.io, Render, or any Python host'
    case 'none':
      return 'N/A (frontend only)'
  }
}

export function renderDeploymentGuide(config: ProjectConfig): string {
  const workerLine = config.includeWorker
    ? '- Deploy `apps/worker` as a separate Railway or container service if background jobs are required.'
    : '- Worker deployment is not needed unless you add the workspace back later.'

  const dockerLine = config.includeDocker
    ? `- Local Docker services are available via \`docker-compose.yml\` for ${config.database === 'postgres' ? 'PostgreSQL and ' : config.database === 'mongodb' ? 'MongoDB and ' : ''}Redis.`
    : `- Local Docker services were not generated; provide managed database and Redis endpoints manually.`

  const serverEnvVars =
    config.backend !== 'none'
      ? `- Server:
  \`PORT\`, \`NODE_ENV\`, ${config.database !== 'none' ? '`DATABASE_URL`, ' : ''}\`REDIS_URL\`, \`FRONTEND_URL\`,
  \`BETTER_AUTH_URL\`, \`BETTER_AUTH_SECRET\``
      : ''

  return `# Deployment Guide

## Stack

- Backend: ${backendLabel(config.backend)}
- Database: ${config.database}
- ORM: ${config.orm}

## Recommended Split

- Deploy \`apps/web\` to Vercel.
${config.backend !== 'none' ? `- Deploy \`apps/server\` to ${hostingHint(config.backend)}.` : '- No backend server to deploy.'}
${workerLine}

## Required Environment Variables

- Web:
  \`NEXT_PUBLIC_APP_URL\`, \`NEXT_PUBLIC_API_URL\`
${serverEnvVars}

## Local Services

${dockerLine}

## Suggested Rollout Order

1. ${config.database !== 'none' ? 'Deploy the database and Redis.' : 'Deploy Redis (or skip if not needed).'}
2. ${config.backend !== 'none' ? 'Deploy the server and validate `/health`.' : 'Skip (no backend).'}
3. Deploy the web app with the correct \`NEXT_PUBLIC_API_URL\`.
4. Add CI gates before the first production release.

## Deploy API to Render

Use the repo \`render.yaml\` blueprint or create a **Web Service** manually:

| Setting | Value |
|---------|--------|
| Root directory | \`.\` (monorepo root) |
| Runtime | Docker **or** Native with Bun |
| Dockerfile | \`apps/server/Dockerfile\` (dockerContext: repo root) |
| Health check | \`/health\` |

**Native Bun (no Docker):**

\`\`\`bash
# Build
bun install
bun run build --filter=@template/server

# Start
cd apps/server && HOST=0.0.0.0 bun run start
\`\`\`

**Required secrets on Render** (Dashboard → Environment):

- \`DATABASE_URL\` — from Render Postgres (or external Neon)
- \`REDIS_URL\` — Render Key Value / Upstash / external Redis (**required** for queues + rate limits)
- \`FRONTEND_URL\` — your Vercel URL (e.g. \`https://my-app.vercel.app\`)
- \`BETTER_AUTH_URL\` — this API's public URL (e.g. \`https://template-api.onrender.com\`)
- \`BETTER_AUTH_SECRET\` — 32+ char random string
- \`PORT\` — set by Render automatically (do not hardcode in service settings)
- \`RENDER=true\` — set automatically on Render

**Common failure causes:**

1. **Service never becomes healthy** — API must listen on \`0.0.0.0\` and \`process.env.PORT\` (fixed in \`apps/server/src/server.ts\`).
2. **Crash on boot** — missing \`REDIS_URL\` or \`DATABASE_URL\`; check Logs tab.
3. **Redis connection at import** — Redis connects during startup, not at module import.
4. **CORS errors** — \`FRONTEND_URL\` must exactly match the browser origin (scheme + host).
5. **Auth callbacks** — \`BETTER_AUTH_URL\` must be the public API URL, not localhost.
6. **Build without Prisma client** — run \`bun install\` at repo root (postinstall runs \`db:generate\`).

After deploy: \`curl https://<your-service>.onrender.com/health\`

## Notes

This guide was generated by \`@arche/create\` for ${config.projectName}.
Review it alongside the repo docs before production rollout.
`
}
